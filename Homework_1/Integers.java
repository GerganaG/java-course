
public class Integers {

	public static void main(String[] args) {
		
		int a = 5;
		int b = 8;
		
		System.out.print("a = ");
		System.out.println(a);
		System.out.print("b = ");
		System.out.println(b);
		
		a = a + b;
		b = a - b;
		a = a - b;
		
		System.out.print("a = ");
		System.out.println(a);
		System.out.print("b = ");
		System.out.println(b);
		
	
	}

}
